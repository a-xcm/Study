var arr4 = [];//空数组
var arr5 = [2,3,4,5,6];

//定义多维数组
var arr6 = [[1,2,3],[4,5],[7]];

console.log(arr1);
console.log(arr2);
console.log(arr3);
console.log(arr4);
console.log(arr5);
